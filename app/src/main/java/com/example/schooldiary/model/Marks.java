package com.example.schooldiary.model;

public class Marks {

    private String name;
    private String marks;

    public Marks() {}

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getMarks() {
        return marks;
    }

    public void setMarks(String marks) {
        this.marks = marks;
    }
}
