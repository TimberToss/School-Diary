package com.example.schooldiary.model;

public class Dates {

    private String name;
    private String date;

    public Dates() {}

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
